# Problem 8 - Student Loans and College Degrees


# Part a
# From the table, the probability that a student with a student loan
# had received a college degree is the column total for "Yes".
# So the probability is 0.42.

0.42

# Part b
# From the table, the probability that a student with a student loan
# had not received a college degree is the column total for "No".
# So the probability is 0.58.

0.58

# Part c
# Given that the student has received a college degree,
# the probability that the student has a delinquent loan is:
# 0.16 / 0.42 = 0.3810

0.16 / 0.42


# Part d
# Given that the student has not received a college degree,
# the probability that the student has a delinquent loan is:
# 0.34 / 0.58 = 0.5862

0.34 / 0.58

# Part e
# Dropping out of college without a degree increases the chance
# of having a delinquent student loan.
# Students without a degree have a delinquent loan probability of 0.5862,
# while students with a degree have a delinquent loan probability of 0.3810.
# So students without a degree are more likely to have a delinquent loan.

0.5862 - 0.3810
